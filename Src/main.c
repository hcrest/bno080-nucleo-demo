/** Configure pins as 
        * Analog 
        * Input 
        * Output
        * EVENT_OUT
        * EXTI
*/
static void MX_GPIO_Init(void)
{

  GPIO_InitTypeDef GPIO_InitStruct;

  /* GPIO Ports Clock Enable */
  __HAL_RCC_GPIOC_CLK_ENABLE();
  __HAL_RCC_GPIOA_CLK_ENABLE();
  __HAL_RCC_GPIOB_CLK_ENABLE();

  /*Configure GPIO pin Output Level */
  HAL_GPIO_WritePin(GPIOB, SH_WAKEN_Pin|SH_RSTN_Pin|SH_BOOTN_Pin|SH_CSN_Pin, GPIO_PIN_RESET);

  /*Configure GPIO pins : SH_WAKEN_Pin SH_RSTN_Pin SH_BOOTN_Pin */
  GPIO_InitStruct.Pin = SH_WAKEN_Pin|SH_RSTN_Pin|SH_BOOTN_Pin;
  GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
  GPIO_InitStruct.Pull = GPIO_NOPULL;
  GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_LOW;
  HAL_GPIO_Init(GPIOB, &GPIO_InitStruct);

  /*Configure GPIO pin : SH_INTN_Pin */
  GPIO_InitStruct.Pin = SH_INTN_Pin;
  GPIO_InitStruct.Mode = GPIO_MODE_IT_FALLING;
  GPIO_InitStruct.Pull = GPIO_PULLUP;
  HAL_GPIO_Init(SH_INTN_GPIO_Port, &GPIO_InitStruct);

  /*Configure GPIO pin : SH_CSN_Pin */
  GPIO_InitStruct.Pin = SH_CSN_Pin;
  GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
  GPIO_InitStruct.Pull = GPIO_NOPULL;
  GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_VERY_HIGH;
  HAL_GPIO_Init(SH_CSN_GPIO_Port, &GPIO_InitStruct);

  /*Configure GPIO pin : SH_CLKSEL0_Pin */
  GPIO_InitStruct.Pin = SH_CLKSEL0_Pin;
  GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
  GPIO_InitStruct.Pull = GPIO_NOPULL;
  GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_VERY_HIGH;
  HAL_GPIO_Init(SH_CLKSEL0_GPIO_Port, &GPIO_InitStruct);

  /* Set CLKSEL0 to 0 : BNO080 should use crystal for timing. */
  HAL_GPIO_WritePin(SH_CLKSEL0_GPIO_Port, SH_CLKSEL0_Pin, GPIO_PIN_RESET);

  /* EXTI interrupt init*/
  HAL_NVIC_SetPriority(EXTI15_10_IRQn, 5, 0);
}

